                <p>The Summit Historical Society (SHS) was incorporated on October 25, 1966, as a private, nonprofit, tax-exempt State of Colorado 501(c) (3) corporation. As such, contributions are tax deductible as permitted by section 170 IRS Code.</p>

                <h3>Mission & Objectives</h3>

                <p>Our mission is to discover, preserve, interpret, and disseminate information to the public about the history of Summit County, Colorado.</p>

                <p>To accomplish this mission, the Society has adopted the following goals and objectives:</p>

                <ul>
                    <li>Bring together people interested in history, especially the history of the Summit County area</li>
                    <li>Discover and collect material to establish or illustrate the history of Summit County--its geology and mineralogy, exploration, settlement, development, environmental conditions, recreational facilities, events, activities, and progress</li>
                    <li>Provide for the preservation of such material and for its accessibility to all who wish to examine or study it</li>
                    <li>Disseminate historical information and stimulate interest in the past</li>
                    <li>Own, equip, maintain, and operate public museums</li>                    
                </ul>

                <p>Please click here for more detailed description of our Goals and Objectives.</p>

                <h3>Organization</h3>

                <p>The Summit Historical Society operates in accordance with:</p>

                <ul>
                    <li>The provisions of Article 19, Chapter 31, Colorado Revised Statutes 1963 (as amended)</li>
                    <li>The bylaws of the Summit Historical Society</li>
                    <li>Authorizations by the Board of Directors of the Society.</li>                    
                </ul>

                <p>Please click here for more detailed description of our Board of Directors and its responsibilities and here for our organization chart.</p>
                
                <p>Paid and volunteer staff carry out the Society's daily activities and programs. All volunteers serve without compensation. Three members constitute a quorum for a business meeting of the Society.</p>

                <p>Please <a href="membership.php">click here to visit our membership site for more information on membership and how to join the Society</a>.</p>

                <h3>Current Programs and Activities</h3>

                <p>The Society fosters heritage preservation through ongoing programs and highly visible projects for direct public benefit. In doing so, the Society provides a showcase for protection and improvement of historical sites in Summit County. Many of these activities result in educational and recreational opportunities through historical tours and roadside guides and improve the quality of the historical sites themselves.</p>

                <p>Through exhibits, publications, lectures, and similar programs, the SHS educates residents and visitors alike about the area's heritage and traditional way of life. In addition to refurbishing, developing, and maintaining the historical sites entrusted to its care, the Society conducts many regularly scheduled tours year-round and special tours upon request. Recent programs have, for instance, benefited elementary school children, senior citizen centers, travel agents, Boy and Girl Scout troops, Elderhostel groups, tourists from around the world, and numerous other local and visiting organizations.</p>

                <p>Please <a href="sites.php">click here to learn about our points of historical interest in Summit County and available SHS tours</a>. Selection of any of the menu items will provide background information and photographs of the sites selected.</p>

                <p>The Society also publishes books, conducts wildflower and historical hikes, and responds to information requests from members, present and past residents, and historical researchers.</p>

                <p>Please visit our "Publications" and "Book Club" menus to see a more detailed listing of available books and Society publications as well as Book Club activities. The Summit Historical Society Book Club is open to the public and meets monthly to discuss works of Western U.S. history, often taking field trips to nearby areas of historical interest.</p>

                <p>Since 1966, the SHS has grown to own, manage, or control many historical sites and over 17,000 artifacts including the following:</p>

                <ul>
                    <li>The 1875 Edwin Carter Museum in Breckenridge</li>
                    <li>The Washington Gold Mine, in Illinois Gulch south of Breckenridge, dating from 1880</li>
                    <li>The 1883 Dillon Schoolhouse in Dillon</li>
                    <li>The Montezuma Schoolhouse, built in 1885, in Montezuma</li>
                    <li>The 1896 William Harrison Briggle Home in Breckenridge</li>
                    <li>A Bucyrus Gold Dredge, dating from 1900, in the Swan River Valley north of Breckenridge</li>
                    <li>The Lomax Placer Mine in Breckenridge</li>
                    <li>The Lula Myers Ranch House, built in 1885</li>
                    <li>The 1918 Rice Barn in Summit Cove, near Keystone</li>
                    <li>A 1930s honeymoon cabin located near the Dillon School House</li>
                    <li>Slate Creek Hall, built in 1936, north of Silverthorne</li>
                    <li>A Hoosier Pass cabin located near the summit of Hoosier Pass</li>
                    <li>Many of these sites have regularly scheduled SHS tours; all may be visited with a knowledgeable SHS guide by prior arrangement. Please visit our "Historical Sites" and "Available Tours" pages to see a detailed description of each of these historical sites and available SHS tours.</li>
                </ul>


