                    <h3>Overview</h3>

                    <p>The Summit Historical Society has a large number of archival materials and artifacts in its possession. Its holdings of historical Summit County photographs, maps, and government documents are extensive. Similarly, there is a growing collection of biographical data concerning Summit County figures. Once estimated at 17,000 separate items, the Society's artifacts collection is enormous and is still growing. Many items are either on display in the Society's properties or are on loan in the area for public view.</p>

                    <h3>To Search the Archives</h3>

                    <p>Summit County Library Search Directions for SHS Collections.  Most items in the Society’s archival collections have been catalogued for online access via the Summit County Library and Marmot systems.</p>

                    </p>To SEARCH for a specific named item, use the following directions:</p>

                    <ol>
                        <li>Using your favorite search engine, search for Summit County Public Library Catalog Home or http://summit.opac.marmot.org</li>
                        <li>When the catalog appears, select searching by either Keyword or Title.  Type in the name of the object of your search.</li>
                        <li>Once the record appears, click on the title/heading for the record for details.</li>
                        <li>Click on the “Notes” tab for a full description of the item.</li>
                        <li>If you see the notation “Buy a Copy,” and you are interested in a photo or map, contact the archives at <a href="mailto:archives@summithistorical.org">archives@summithistorical.org</a>.</li>
                    </ol>

                    <p>To BROWSE the Society’s catalogued collections, use the following directions:</p>

                    <ol>
                        <li>Using your favorite search engine, search for Summit County Public Library Catalog Home or http://summit.opac.marmot.org</li>
                        <li>Under Keyword search, type one of the following:
                            <ul>
                                <li>For photographs:  summit county (colo) historical society photographs</li>
                                <li>When the listing appears, click Narrow Your Results to Photo.   For an alphabetical listing, sort by Title.</li>
                            </ul>
                        </li>

                        <li>For maps: summit county (colo) historical society maps This is a work in progress.  Some maps will have a map  icon; others will have a book icon.  For an alphabetical listing, sort by Title and scroll to “Abe Lee placer.”</li>

                        <li>For manuscripts, government records and books: summit county (colo) historical society manuscripts.  This is another work in progress.  When the listing appears, click Entire Collection.  For an alphabetical listing, sort by Title.</li>

                        <li>For the Burke-Martin Collection:  summit county (colo) historical society burke-martin mine reports, click Books for records; click Photo for photograph; click Map for maps.</li>

                        <li>For an overview of the Society’s entire catalogued archival holdings:  Dillon Schoolhouse Museum</li>
                    </ol>

