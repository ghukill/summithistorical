	<h1>Dillon Dam Glory Hole</h1>

	<p>Constructed in the late 1950's and 1960's, the Glory Hole at the western end of the dam is the overflow outlet for the reservoir.  The top of the Glory Hole has an elevation of 9017 feet.  Water entering the Glory Hole drops 233 feet before making a 90 degree bend toward the north.  The water flows through a 1700-foot long tunnel.  The tunnel, with a diameter of 15 feet, can carry 15,000 cfs if the lake reaches an elevation of 9025 feet.  The concrete fins on the Glory Hole prevent water from swirling as it enters the hole, causing the concrete to erode.  The Glory Hole, tunnel and outlet works on the north side of the dam are made of highly polished concrete.</p>

	<p>To learn more about the Glory Hole, Dillon reservoir and dam, and “old” Dillon, read the book, Dillon, Denver, and the Dam, a Summit Historical Society publication. </p>

	<p><strong>Directions:</strong> To see the Glory Hole, park in the overlook parking lot at the western end of the dam and walk a short distance north on the recreation path.</p>
		
