		<h1>Antique Fire Truck</h1>
		
		<p>In January, 2005, Chief Winston of the Lake Dillon Fire and Rescue Station #8 donated this long-retired 1942 Sterling-Howe fire truck to the Summit Historical Society. With its Sterling chassis and Howe fire apparatus body, the truck is configured as a pumper.  Originally purchased new by the U.S. Army in 1942 and subsequently transferred to the Dillon Volunteer Fire Department, it carried fire fighters to many emergencies in Summit County.</p>

		<p>To accommodate the truck, town employees cut an entrance in the wall of the town hall in Dillon #3 and installed a garage door.  Town Council members often sat on the running boards of the fire truck while conducting town business.  The truck lost its home when the town hall became the Lake Dillon Theatre Company.</p>

		<p>Taken out of storage by the Historical Society, the truck received many hours of tender loving care from Society members and volunteers before it could run again.  It has participated in many special events throughout the years.</p>

