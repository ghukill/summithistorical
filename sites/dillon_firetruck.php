		<h1>Antique Fire Truck</h1>
		
		<p>This fire truck was donated to the Summit Historical Society in January of 2005 by Chief Winston of the Lake Dillon Fire & Rescue Department. It had been retired from service for many years and had been in storage for many years after that.</p>
		
		<p>The truck is a 1942 Sterling chassis with a Howe fire apparatus body mounted on it, and is configured as a pumper. It has been serving in Summit County, Colorado for it's entire life. The fire truck was originally purchased new by the U.S. Army in 1942, and was subsequently transferred to the Dillon Volunteer Fire Department sometime after that.</p>
		 
		<p>In it's early life, the truck was garaged in the Dillon Town Hall. A hole had to be cut, and a garage door had to be installed in the side of the Town Hall building to accommodate this.  It is said that many Dillon Town Council meetings were held while sitting on the running boards of the old fire truck! The Dillon Town Hall was moved up the hill to its present location when the old town of Dillon was inundated by the new Dillon Dam and Reservoir that was built in 1962. The old Dillon Town Hall building now houses the Lake Dillon Theater Company at 146 Lake Dillon Drive.</p>
		 
		<p>The fire truck was not in running condition when received by the Summit Historical Society in January of 2005. It has been the result of many hours of hard work by Society members and volunteers that it was able to make it's debut, and be seen running again by the public in the Town Of Dillon 4th of July parade in 2005.  The truck won an award for 'First Place, Judge's Choice' in that parade.</p>
		 
		<p>It is hoped that the old fire truck will add excitement to parades and events, and a sense of community pride in Summit County for years to come.</p>

