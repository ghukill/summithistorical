		<h1>Rice Barn</h1>

		<p>Benjamin Rice and his three sons constructed this two-story 56’ X 28’ barn between 1918 and 1920.  The log structure sits on a rock and masonry foundation that originally elevated the floor two feet above the ground.  The lower story’s two-inch-thick floorboards easily supported the weight of 20 draft horses, their feed, wagons, and supplies.  Corrugated tin covers the original gambrel board-and-batten roof.  The exposed rafters and original board roof remain visible.</p>

		<p>The upper story, the mow, held 40 tons of loose hay, fed to the work horses on the Rice ranch and to those pulling the freight wagons and stage coaches operated by Benjamin Rice.</p>

		<p>An important stop on the Dillon-Kremmling stagecoach line, the barn with its 104-inch-wide front and rear sliding doors allowed teams to be driven directly into the barn for repairs.  Inside, fresh teams replaced those tired from the long haul.</p>

		<p>Visitors to the barn see tack rooms, carriages, a sleigh, grinding wheels, and other ranching equipment.</p>

		<p><strong>Directions: </strong>You will find the barn at 357 Cove Boulevard in Summit Cove between Dillon and Keystone.</p>

