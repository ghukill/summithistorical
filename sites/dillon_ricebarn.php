		<h1>Rice Barn</h1>

		<p>Benjamin Rice and his three sons constructed this two-story, 56 ft. x 28 ft. barn between 1918 and 1920. The log structure sits on a rock-and-masonry foundation that originally elevated the floor two feet above the ground. Rags are visible in the masonry chinking. The original gambrel board-and-batten roof is covered with corrugated tin; three of the five windows are covered with expanded metal.</p>

		<p>The barn's upper story is a mow that held 40 tons of loose hay, which was voraciously consumed by the work horses on Ben's ranch and by those that worked in Ben's freighting business, pulling stagecoach-line wagons. A climb up the ladder to the loft is rewarded by the sight of exposed rafters and the original board roof. The lower story's 2-inch-thick floorboards easily supported the weight of 20 draft horses, their feed, wagons and other equipment and supplies. The barn was an important stop on the Dillon-Kremmling stagecoach line. Its 104"-wide front and rear sliding doors accommodated with ease the teams of horses driven into the barn where wagon repairs were made and where the coaches were outfitted with fresh teams.</p>

		<p>The barn had two tack rooms and a granary. Some of its stalls are filled with old saddles, bridles, grinding wheels and other ranching paraphernalia. Ropes hang from heavy beams. An old carriage greets visitors when they enter the barn.</p>

		<p><strong>Directions:</strong> The Rice Barn is located at 357 Cove Blvd. in Summit Cove between Dillon and Keystone.</p>

