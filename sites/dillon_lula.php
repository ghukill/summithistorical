		<h1>Lula Myers Cabin</h1>
		<p>This two-story 16’X24’ log structure, built in 1885 by Charles Delker under the Federal Homestead Act, sat along the bank of the Snake River near Keystone.  The Society moved it to its present site November 5, 1976.  Sticks, rocks, clay, and rags served as chinking, filling the spaces between the hand-hewn, square logs.  Coffee cans, rolled flat, cover the chinking on the outside.  Inside, layers of newspaper provided extra insulation from the cold.</p>

		<p>Two well-known families, important to Summit County, lived in the house—the Delker and Myers families.  Both men made most of their money in mining-related businesses.  The kitchen in the cabin tells of the daily life of the women who lived there—“Nurse” Delker and Lula Myers, a “schoolmarm” for a short time in Frisco.</p>

		<p><strong>Directions:</strong> You will find the Myers Cabin behind the Schoolhouse Museum at 403 LaBonte Street in Dillon.</p>
