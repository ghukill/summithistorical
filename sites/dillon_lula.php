		<h1>Lula Myers Ranch House</h1>
		<p>This two-story, 16-ft. x 24-ft. log house was built in 1885 by Charles Delker under the Federal Homestead Act. Its original location was near the bank of the Snake River near Keystone; it was moved to its present site on Nov. 5, 1976. The logs are hand hewn with an ax and chinked with sticks, rocks, clay and rags. Coffee cans, rolled flat, are nailed over the chinking on the outside. Inside, the chinking is covered with layer upon layer of newspapers glued to the logs for added insulation from the cold.</p>

		<p>The house was occupied by two important and well-known Summit County families - the Delkers and the Myerses. Both men made most of their money in mining-related businesses. Many parties and social events were held in this house. Kitchen paraphernalia, so important to the preparation of a large and delicious spread, are on display. Tour-takers are taken with the handsome cookstove and with the comfortable, cozy atmosphere that pervades the house.</p>

		<p><strong>Directions:</strong> The Lula Myers Ranch House is located at 403 LaBonte Street in Dillon, behind the Dillon Schoolhouse.</p>
