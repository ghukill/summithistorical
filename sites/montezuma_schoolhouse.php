		<h1>Montezuma Schoolhouse</h1>
		
		<p>At 10,400 feet, the Montezuma Schoolhouse is one of the highest school buildings in the nation. This frame, clapboarded structure was the town's second schoolhouse. The opening of silver mines in the area caused the town's population to increase so rapidly that the first school, built in 1880, soon became too small to house the growing number of students. This larger structure opened in 1884; the belfry, bell and entry hall were added later. Protestants held services in the building (the Catholics had their own church), and they greatly appreciated the purchase of an organ, made possible by funds raised through programs and box-supper socials.</p>

		<p>While visitors like to sit at the one-room school's original desks and leaf through primers, what they prefer to do is check out the two attached two-seater “outhouses,” one for girls and one for boys. The doorsto these facilities were accessible from inside the school, so perhaps outhouse does not exactly describe the interior additions. These double-seaters were used by pupils and teachers alike until the school closed in 1958.</p>

		<p>The schoolhouse undoubtedly had a ladder or two on its roof, for town rules required that all buildings be so equipped to aid in fire fighting. Despite the ladders and the tin collars around all chimneys and smoke stacks, also required by town law, Montezuma suffered considerable fire damage in 1915, 1949 and 1958.</p>

		<p><strong>Directions:</strong> The Montezuma Schoolhouse is located on the hillside east of Main Street in the town of Montezuma.</p>
