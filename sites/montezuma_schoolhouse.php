		<h1>Montezuma Schoolhouse</h1>
		
		<p>The Montezuma Schoolhouse stands at 10,400 feet above sea level, making it one of the highest school buildings in the country.  A smaller school, built in 1880, preceded this frame structure built in 1884.   Workers added the belfry, bell and entry hall after 1884. Subsequently, white clapboard covered the original brown board-and-batten walls.</p>

		<p>Protestants worshipped in the building.  Using money raised through programs and box-supper socials, they purchased an organ for their services.</p>

		<p>Visitors enjoy visiting the two attached two-seater “outhouses,” one for the boys and one for the girls, found at the back of the building.  Accessible from inside the schoolroom, pupils and teachers alike used these two-seaters until the school closed in 1958.</p>

		<p>The school undoubtedly had ladders on its roof in case of fire.  Town rules required this as a fire-fighting method.  Town ordinances also called for tin collars around all chimneys and smoke stacks.  Despite the precautions, Montezuma suffered considerable fire damage in 1915, 1949, and 1958.</p>

		<p><strong>Directions: </strong>You can find the Montezuma Schoolhouse on the hillside east of Main Street in the town of Montezuma.</p>
