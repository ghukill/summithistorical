		<h1>Honeymoon Cabin</h1>

		<p>Most likely, this one-room log cabin was built without assistance by one man in the 1930s. It is in a style found in Scandinavia, with peeled logs that are very large at the base, becoming smaller as the walls increase in height. The door is very small - to keep the heat in, not because the owner was short. The caulking is cement on the outside. Inside, newspapers and rags add additional insulation from the cold, and slender aspen boughs, nailed between the logs, afford extra protection from the elements. One wall has no opening; this wall and all the corners were packed with snow to improve the insulation. The roof is corrugated galvanized steel.</p>

		<p>This is a "survival" cabin, a descriptive name that came out of the Great Depression. When there were no jobs available, a lumberman could build his own home single-handedly, cut down trees in the nearby forest for fuel to burn in a stove that served as a heater as well as a griddle/oven, cultivate some vegetables in the short growing season and survive on hunting and fishing. Screens, like the one in the cabin, were used to divide the one room into areas so the inhabitants had some privacy.</p>

		<p><strong>Directions:</strong> The Honeymoon Cabin is located at 403 LaBonte Street in Dillon, behind the Dillon Schoolhouse.</p>
